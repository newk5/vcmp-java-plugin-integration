package com.maxorator.vcmp.java.plugin.integration.server;

public class MapBounds {
    public float maxX;
    public float minX;
    public float maxY;
    public float minY;

    public MapBounds(float maxX, float minX, float maxY, float minY) {
        this.maxX = maxX;
        this.minX = minX;
        this.maxY = maxY;
        this.minY = minY;
    }
}
